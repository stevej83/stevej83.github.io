function toggle208(value) {
    if (value == 'showBad') {
        document.getElementById('popTxt208b').style.display = 'block';
		document.getElementById('popTxt208b2').style.display = 'none';
        document.getElementById('badTxt208').style.display = 'block';
        document.getElementById('popTxt208g').style.display = 'none';
        document.getElementById('goodTxt208').style.display = 'none';
    }
	else if (value == 'showBad2') {
        document.getElementById('popTxt208b').style.display = 'none';
		document.getElementById('popTxt208b2').style.display = 'block';
        document.getElementById('badTxt208').style.display = 'block';
        document.getElementById('popTxt208g').style.display = 'none';
        document.getElementById('goodTxt208').style.display = 'none';
    }
    else if (value == 'showGood') {
        document.getElementById('popTxt208b').style.display = 'none';
		document.getElementById('popTxt208b2').style.display = 'none';
        document.getElementById('badTxt208').style.display = 'none';
        document.getElementById('popTxt208g').style.display = 'block';
        document.getElementById('goodTxt208').style.display = 'block';
    }
    else {
        document.getElementById('popTxt208b').style.display = 'none';
		document.getElementById('popTxt208b2').style.display = 'none';
        document.getElementById('badTxt208').style.display = 'none';
        document.getElementById('popTxt208g').style.display = 'none';
        document.getElementById('goodTxt208').style.display = 'none';
    }
}

